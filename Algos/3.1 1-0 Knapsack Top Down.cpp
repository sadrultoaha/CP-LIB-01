int dp[MAX][MAX], P[]={60, 100, 120},  W[] = {10, 20, 30},N;
int knps(int i, int w) {
	if (i >= N || w == 0)return 0;
	if (dp[i][w] != -1)return dp[i][w];
	int p1 = 0, p2 = 0;
	if (W[i] <= w)p1 = P[i] + knps(i + 1, w - W[i]);
	p2 = knps(i + 1, w);
	return dp[i][w] = max(p1, p2);
    }

